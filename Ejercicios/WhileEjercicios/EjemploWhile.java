package Ejercicios.WhileEjercicios;

public class EjemploWhile {
    /*
     * La estructura repetitiva While entonces se repite mientras la condición sea
     * Verdadera
     * cuando la condición es Falsa entonces se corta.
     */
    public static void main(String[] args) {

        int i = 0;
        while (i < 11) {
            System.out.println("El valor de i: " + i);
            i = i + 1;

        }
    }

}
